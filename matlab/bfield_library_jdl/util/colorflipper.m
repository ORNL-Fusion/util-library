if only_last
    cmap(1,1:3) = [1 1 1];
else
    iblue = find(ismember(cmap, [0 0 1], 'rows'));
    cmap(1:iblue,1) = linspace(1,0,iblue);
    cmap(1:iblue,2) = linspace(1,0,iblue);
    cmap(1:iblue,3) = 1;
end
end

