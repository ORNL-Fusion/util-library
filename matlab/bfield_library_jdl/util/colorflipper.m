function cmap = Greens(n)
    if nargin < 1
        n = 256; 
    end   
    x = linspace(0,1,n)';              
    % cmap = [ (1-x), ones(n,1), (1-x)]; 
        g = 1 - 0.7*x;                     
    cmap = [1-x, g, 1-x];               
end


