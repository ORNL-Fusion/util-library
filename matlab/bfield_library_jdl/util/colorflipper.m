function cf = colorflipper(num_colors,cmap)
% cf = colorflipper(num_colors,cmap)
% cf(num_colors,3)
% Available options for cmap:
%'jet'
%'jet_to_white'
%'jet_white_last'
%'rainbow_to_white'
%'rainbow_white_last'
%'parula'
%'hsv'
%'cw'  -- cool warm
%'rb'
%'blues'
%'morgan'
%'rainbow'
%'magma'
%'magma_to_white'
%'plasma_to_white'
%'plasma'
%'matlab_lines'
%'tab20'
%'viridis'
%'batlowW'
%'greens'
%'devon'

if nargin < 1
    num_colors = 64;
end
if nargin < 2
    cmap = 'jet';
end

switch lower(cmap)
    case {'jet'}
        if isempty(num_colors)
            num_colors = 64;
        end
        cf = jet(num_colors);
        return;
    case {'jet_to_white'}
        cf = cmap_jet_to_white(num_colors);
        return;
    case {'jet_white_last'}
        cf = cmap_jet_to_white(num_colors,1);
        return;        
    case {'rainbow_to_white'}
        cf = cmap_rainbow_to_white(16);
    case {'rainbow_white_last'}
        cf = cmap_rainbow_to_white(1);        
    case {'parula'}
        cf = parula(num_colors);
        return;
    case {'hsv'}
        cf = hsv(num_colors);
        return;
    case {'cw'}
        cf = cmap_cw;
    case {'rb'}
        cf = cmap_rb;
    case {'blues'}
        cf = cmap_blues;
    case {'morgan'}
        cf = cmap_morgan;
    case {'rainbow'}
        cf = cmap_rainbow;
    case {'magma'}
        cf = cmap_magma;
    case {'magma_to_white'}
        cf = cmap_magma_to_white(16);
    case {'plasma_to_white'}
        cf = cmap_plasma_to_white(3);        
    case {'plasma'}
        cf = cmap_plasma;
    case {'matlab_lines'}
        cf = matlab_lines(num_colors);
        return;
    case {'tab20'}
        cf = cmap_tab20;                
    case {'viridis'}
        cf = viridis(num_colors);
        return;
    case {'batloww'}
        cf = batlowW;
    case {'greens'}
        cf = Greens(num_colors);
    case {'devon'}
        cf = devon();
    otherwise
        cf = matlab_lines(num_colors);
        return;
end

if ~isempty(num_colors)
    cf = interpolate_cf(cf,num_colors);
end

end


